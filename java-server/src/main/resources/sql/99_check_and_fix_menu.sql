USE java_2_48;

-- ===============================================================
-- 1. 先查看个人中心的所有菜单
-- ===============================================================
SELECT * FROM menu WHERE pid = 300 ORDER BY id ASC;

-- ===============================================================
-- 2. 更新已有的"我的关注"菜单项（id=304）
-- ===============================================================
UPDATE menu 
SET name = 'MyFollowing'
WHERE id = 304;

-- ===============================================================
-- 3. 更新或插入"我的粉丝"菜单项
-- ===============================================================
-- 先检查 id=309 是否存在，如果存在则更新，否则插入
INSERT INTO menu (id, pid, name, title, user_type_ids) 
VALUES (309, 300, 'MyFollowers', '我的粉丝', '1,2,3')
ON DUPLICATE KEY UPDATE
    name = VALUES(name),
    title = VALUES(title),
    user_type_ids = VALUES(user_type_ids);

-- ===============================================================
-- 4. 再次查看个人中心的所有菜单确认
-- ===============================================================
SELECT * FROM menu WHERE pid = 300 ORDER BY id ASC;

